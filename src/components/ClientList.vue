import { onMounted, ref } from "vue";

export default {
    setup() {
        const isMobile = ref(false);

        onMounted(() => {
            if (window.innerWidth < 550) {
                isMobile.value = true;
            } else {
                isMobile.value = false;
            }

            window.addEventListener(
                "resize",
                () => {
                    if (window.innerWidth < 550 && !isMobile.value) {
                        isMobile.value = true;
                    } else if (window.innerWidth >= 550 && !!isMobile.value) {
                        isMobile.value = false;
                    }
                },
                true
            );
        });

        return {
            isMobile,
        };
    },
};
